								<h3>Directives et procédures de soumissions</h3>
								<p>Manuscripts should be prepared in 10-point font using the IEEE 8.5" x 11" two-column format. All papers should be in PDF
								format, and submitted electronically at Paper Submission Link. A full paper must not exceed the stated length
								(including all figures, tables and references). Submitted papers must present original unpublished research that is not
								currently under review for any other conference or journal. Papers not following these guidelines may be rejected without
								review. Also submissions received after the due date, exceeding length limit, or not appropriately structured may also not be
								considered. Authors may contact the Program Chair for further information or clarification.</p>

								<p>All submissions are peer-reviewed by at least three reviewers. Accepted papers will appear in the SDS Proceedings,
								and be published by the IEEE Computer Society Conference Publishing Services and be submitted to IEEE Xplore for inclusion.</p>

								<p>Submitted papers must include original work, and must not be under consideration for another conference or journal.
								Submission of regular papers must not exceed 6 pages and must follow the IEEE paper format. Please include up to 7 keywords,
								complete postal and e-mail address, and fax and phone numbers of the corresponding author. Authors of accepted papers are
								expected to present their work at the conference. Submitted papers that are deemed of good quality but that could not be
								accepted as regular papers will be accepted as short papers. Length of short papers.</p>

