<h2>Cloud Computing Conferences</h2>
<article>


  <p>Next generation cloud systems will require a paradigm shift in how they are constructed and managed.
  Conventional control and management platforms are facing considerable challenges regarding flexibility,
  dependability and security that next generation systems will require. The cloud computing paradigm has gone part
  of the way towards alleviating some of the problems associated with resource allocation, utilization and managements (e.g., via elasticity).
  However, many of the elements of a well-designed cloud environment remain “stiff” and hard to modify and adapt in an integrated fashion.
  This includes underlying networking topologies, many aspects of the user control over IaaS, PaaS or SaaS layers when such is needed,
  construction of XaaS services, provenance and meta-data collection, and so on. In many situations the problem may be because service abstraction is inadequate.<p>							Software Defined Systems (SDS) are systems that have added software components which help abstract actual IT equipment and other layers. One classical example, of course, are hypervisors. Such separation provides a great opportunity for system administrators to more easily construct and managing their systems through flexible software layers.

  <p>Software Defined Systems include Software Defined Networking (SDN), Software Defined Storage, Software Defined Servers
  (Virtualization), Software Defined Datacenters (SDD), Software Defined Security (SDSec), and ultimately Software Defined Clouds
  (SDCloud) to name a few possibilities. Individual solutions and seamless integration of these abstractions remains in many
  respects a challenge. After the high success of the previous editions (SDS'2014, SDS'2015 and SDS'2016), The fourth International
  confernce on Software Defined Systems 2017 (SDS'2017) will continue to be a forum for scientists, researchers, students,
  and practitioners to present their latest research results, ideas, and developments in the area of software defined systems –
  both components and their integration - and by implication advancement of next generation clouds.</p>

  SDS 2017 aims to investigate the opportunities and in all aspects of Software Defined Systems. In addition, it seeks for novel contributions that help mitigating SDS challenges. That is, the objective of SDS 2017 is to provide a forum for scientists, engineers, and researchers to discuss and exchange new ideas, novel results and experience on all aspects of Software Defined Systems. Researchers are encouraged to submit original research contributions in all major areas, which include, but not limited to:

  * Software Defined Systems support for Cloud Computing .
  * Software Defined Networking (SDN).
  * SDN concepts, architecture, and APIs.
  * Network Virtualization
  * SDN and OpenFlow protocol
  * Software Defined Radio
  * Cognitive Radio Networks .
  * Access Control models in SDN.
  * Software Defined Storage.
  * Storage Automation and Abstraction.
  * Policy-driven storage provisioning.
  * Software Defined Servers and Virtualization.
  * Software Defined Datacenters.
  * Software Defined Security o Security policies automation.
  * Self-management systems.
  * Autonomic Computing techniques.
  * Real-time load prediction model to optimize the user satisfaction.
  * Software Defined Systems Scalability.
  * Software Defined Systems optimization.
  * Software tools and frameworks to support SDS .
  * Software Defined Systems challenges and opportunities
  * Software Defined Systems surveys .
  * Social engineering, insider threats system for SDS.
  * Incident Handling and Penetration Testing with SDS.
  * Software Defined Systems support if IoT.
  * Security protocols in SDS.
  * Security and privacy of mobile SDS based cloud computing
  * Service-oriented architectures, service portability andP2P
  * Network virtualization and cloud-based radio access networks</p>
